<script lang="ts">
  import { activeSidebarItem, navigate, rightSidebarTab } from "../../stores/ui.svelte";

  interface NavItem {
    id: string;
    label: string;
    icon: string;
    path: string;
  }

  const navItems: NavItem[] = [
    { id: "home", label: "Home", icon: "home", path: "/" },
    { id: "repos", label: "Repos", icon: "folder", path: "/repos" },
    { id: "editor", label: "Editor", icon: "edit", path: "/editor" },
    { id: "notebook", label: "Notebook", icon: "book", path: "/notebook" },
    { id: "chat", label: "Kernel AI", icon: "cpu", path: "/editor" },
    { id: "settings", label: "Settings", icon: "settings", path: "/settings" },
  ];

  function handleClick(item: NavItem) {
    if (item.id === "chat") {
      navigate("/editor");
      rightSidebarTab.value = "chat";
    } else {
      navigate(item.path);
    }
  }
</script>

<nav class="flex w-14 flex-col items-center border-r border-base-700 bg-base-800 py-3 gap-1">
  {#each navItems as item}
    <button
      onclick={() => handleClick(item)}
      class="group flex flex-col items-center justify-center w-10 h-10 rounded-lg transition-colors
        {activeSidebarItem.value === item.id
          ? 'bg-accent/20 text-accent'
          : 'text-base-400 hover:bg-base-700 hover:text-base-200'}"
      title={item.label}
    >
      {#if item.icon === "home"}
        <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M3 9l9-7 9 7v11a2 2 0 01-2 2H5a2 2 0 01-2-2z"/><polyline points="9 22 9 12 15 12 15 22"/></svg>
      {:else if item.icon === "folder"}
        <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M22 19a2 2 0 01-2 2H4a2 2 0 01-2-2V5a2 2 0 012-2h5l2 3h9a2 2 0 012 2z"/></svg>
      {:else if item.icon === "edit"}
        <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M11 4H4a2 2 0 00-2 2v14a2 2 0 002 2h14a2 2 0 002-2v-7"/><path d="M18.5 2.5a2.121 2.121 0 013 3L12 15l-4 1 1-4 9.5-9.5z"/></svg>
      {:else if item.icon === "cpu"}
        <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><rect x="4" y="4" width="16" height="16" rx="2"/><rect x="9" y="9" width="6" height="6"/><line x1="9" y1="1" x2="9" y2="4"/><line x1="15" y1="1" x2="15" y2="4"/><line x1="9" y1="20" x2="9" y2="23"/><line x1="15" y1="20" x2="15" y2="23"/><line x1="20" y1="9" x2="23" y2="9"/><line x1="20" y1="14" x2="23" y2="14"/><line x1="1" y1="9" x2="4" y2="9"/><line x1="1" y1="14" x2="4" y2="14"/></svg>
      {:else if item.icon === "book"}
        <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M2 3h6a4 4 0 0 1 4 4v14a3 3 0 0 0-3-3H2z"/><path d="M22 3h-6a4 4 0 0 0-4 4v14a3 3 0 0 1 3-3h7z"/></svg>
      {:else if item.icon === "settings"}
        <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><circle cx="12" cy="12" r="3"/><path d="M19.4 15a1.65 1.65 0 00.33 1.82l.06.06a2 2 0 010 2.83 2 2 0 01-2.83 0l-.06-.06a1.65 1.65 0 00-1.82-.33 1.65 1.65 0 00-1 1.51V21a2 2 0 01-2 2 2 2 0 01-2-2v-.09A1.65 1.65 0 009 19.4a1.65 1.65 0 00-1.82.33l-.06.06a2 2 0 01-2.83 0 2 2 0 010-2.83l.06-.06A1.65 1.65 0 004.68 15a1.65 1.65 0 00-1.51-1H3a2 2 0 01-2-2 2 2 0 012-2h.09A1.65 1.65 0 004.6 9a1.65 1.65 0 00-.33-1.82l-.06-.06a2 2 0 010-2.83 2 2 0 012.83 0l.06.06A1.65 1.65 0 009 4.68a1.65 1.65 0 001-1.51V3a2 2 0 012-2 2 2 0 012 2v.09a1.65 1.65 0 001 1.51 1.65 1.65 0 001.82-.33l.06-.06a2 2 0 012.83 0 2 2 0 010 2.83l-.06.06A1.65 1.65 0 0019.4 9a1.65 1.65 0 001.51 1H21a2 2 0 012 2 2 2 0 01-2 2h-.09a1.65 1.65 0 00-1.51 1z"/></svg>
      {/if}
    </button>
  {/each}
</nav>
